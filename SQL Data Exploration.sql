-- Total Cases vs Total Deaths
-- Shows the likelihood of death if you contract Covid-19 in a particular country (Malaysia)
Select Location, date, total_cases,total_deaths, (total_deaths/total_cases)*100 as DeathPercentage
From PortfolioProject..CovidDeaths
where location like '%Malaysia%'
order by 1,2

-- Total Cases vs Total Population
-- Shows the percentage of population infected with Covid-19
Select Location, date, population, total_cases, (total_cases/population)*100 as PercentagePopulationInfected
From PortfolioProject..CovidDeaths
--where location like '%Malaysia%'
order by 1,2


-- Countries with the Highest Infection Rate compared to their Population

Select Location, population, MAX(total_cases) as HighestInfectionRate, MAX((total_cases/population))*100 as PercentagePopultationInfected
From PortfolioProject..CovidDeaths
--Where location like '%Malaysia%'
Group by Location, population
order by PercentagePopultationInfected desc


-- Countries with Highest Death Count per Population

Select Location, Max(cast(Total_deaths as int)) as TotalDeathCount
From PortfolioProject..CovidDeaths
--Where location like '%Malaysia%'
where continent is not null
Group by Location
order by TotalDeathCount desc

-- Breakdown by Continent
-- Showing contintents with the highest death count per population
Select continent, MAX(cast(Total_deaths as int)) as TotalDeathCount
From PortfolioProject..CovidDeaths
--Where location like '%states%'
